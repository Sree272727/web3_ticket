// Visibility filter for notifications by persona
export function getVisibleNotifications(notifications, persona) {
  switch (persona.id) {
    case 'tcs-admin':
    case 'tcs-employee':
      return notifications;
    case 'customer-admin':
      return notifications.filter(n =>
        n.targetPersonas.includes('customer-admin') &&
        (n.companyId === null || n.companyId === persona.companyId)
      );
    case 'customer-employee':
      return notifications.filter(n =>
        n.targetPersonas.includes('customer-employee') &&
        (n.companyId === null || n.companyId === persona.companyId)
      );
    case 'account-manager':
      return notifications.filter(n =>
        n.targetPersonas.includes('account-manager') &&
        (n.companyId === null || (persona.assignedCompanies || []).includes(n.companyId))
      );
    default:
      return notifications;
  }
}
